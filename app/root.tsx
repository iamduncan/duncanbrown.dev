import type { LinksFunction, MetaFunction } from '@remix-run/node';
import {
  Links,
  LiveReload,
  Meta,
  Outlet,
  Scripts,
  ScrollRestoration,
} from '@remix-run/react';
import { Navbar } from './components/navbar';
import { Sidebar } from './components/sidebar';

import styles from './styles/tailwind.css';

